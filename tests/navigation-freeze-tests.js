#!/usr/bin/env node
"use strict";
const fs=require("fs");
const index=fs.readFileSync("index.html","utf8");
const app=fs.readFileSync("app.js","utf8");
const nav=fs.readFileSync("nav-core.js","utf8");
const boot=fs.readFileSync("boot.js","utf8");
const experience=fs.readFileSync("experience.js","utf8");
const sw=fs.readFileSync("service-worker.js","utf8");
function assert(ok,message){if(!ok)throw new Error(message)}
const runtimePosition=index.indexOf("runtime-config.js?v=6.2.0");
const sourcePosition=index.indexOf("data-source.js?v=6.2.0");
const navPosition=index.indexOf("nav-core.js?v=6.2.0");
const bootPosition=index.indexOf("boot.js?v=6.2.0");
assert(runtimePosition>=0&&sourcePosition>runtimePosition,"Runtime config must load before the database source.");
assert(navPosition>sourcePosition&&bootPosition>navPosition,"Database source and immediate navigation must load before the app bootloader.");
assert(index.includes("experience.js?v=6.2.0"),"The lightweight first-run experience must be present.");
assert(!/community-features\.js|backend-client\.js|supabase-js|data\.js(?:\?|\")/i.test(index),"Retired startup layers must not return.");
assert(index.includes('id="zeus-launch"'),"The lightweight Zeus launch must be present.");
assert(index.includes("pointer-events:none"),"The Zeus launch must never block taps.");
assert(nav.includes("window.BETYNZ_APP_READY"),"Early navigation must hand control to the main app after startup.");
assert(nav.includes("window.history.pushState"),"Early navigation must use browser history directly.");
assert(app.includes("let settledHistory="),"Settled-result data must remain mutable for database refreshes.");
assert(app.includes("window.history[method]"),"Main navigation must use window.history.");
assert(boot.includes("FIRST_PAINT_WAIT_MS=1800"),"The first app paint must have a bounded database wait.");
assert(boot.includes("BetynzDataSource"),"The bootloader must wait for the live database source.");
assert(experience.includes("requestIdleCallback"),"The tour must wait for idle time rather than block startup.");
assert(sw.includes("betynz-shell-v6.2.0"),"The service-worker cache must use the v6.2 version.");
assert(!/data\.js|isVerifiedDataText|DATA_KEY/.test(sw),"The service worker must not parse the old giant dataset.");
console.log("Betynz v6.2 navigation, startup and PWA regression checks passed.");
